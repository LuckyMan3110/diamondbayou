                        <p>A jeweler may describe the weight of a diamond below one carat by its ‘points’ alone. For instance, the jeweler may refer to a diamond that weighs 0.25 carats as a ‘twenty-five pointer’. Diamond weights greater than one carat are expressed in carats and decimals. A 1.08 carat stone would be described as ‘one point oh eight carats’.</p>
                        <p>All else being equal, diamond price increases with carat weight, because larger diamonds are more rare and more desirable. But two diamonds of equal carat weight can have very different values (and prices) depending on three other factors of the 4Cs: <a href="<?php echo SITE_URL; ?>/fourcs/clarity/">Clarity</a>, <a href="<?php echo SITE_URL; ?>/fourcs/color/">Color</a>, and <a href="<?php echo SITE_URL; ?>/fourcs/cut/">Cut</a>.</p>
                        <p>It’s important to remember that a diamond’s value is determined using all of the 4Cs, not just carat weight.</p>
