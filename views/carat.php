                        <p>The carat measurement of a diamond is often confused with the size of the diamond, when in reality it is the weight of the diamond. As a diamond’s weight increases, the price per carat increases. The larger diamonds also have higher color, clarity and a more defined cut.</p>
                        <p>It takes 100 points to equal 1 ct. (carat). &nbsp;Diamonds that are called ¾ ct. can be between 73-78 points. &nbsp;All fractions have a range of points that they can be. &nbsp;A diamond’s weight is measured to the third decimal place (one/thousandths). &nbsp;A thousandth of a carat, and rounded off to the nearest hundredth.</p>
                        <p>Today we weight diamonds using the metric system. &nbsp;These weights are still translated into the carat as the unit of diamond weight. &nbsp;We use sophisticated equipment like the electronics balance. &nbsp;We also have formulas for determining the weight of a stone in a mounting using size measurements. &nbsp;Some of the devices used to give such accurate measurements are the screw micronmeter, the MOH gauge and the Leverage Gauge, to mention a few.</p>
