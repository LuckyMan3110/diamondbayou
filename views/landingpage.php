<script src="<?php echo SITE_URL; ?>images/promotions/OwlCarousel2-2.3.4/dist/owl.carousel.min.js"></script>
<script>
$(function() {
	$('.featured-block__icon--inside').on('click', function() {
		$(this).closest('.lp-featured-block').addClass('slider--active');
		$('.lp-featured-block__slider').owlCarousel({
			loop: true,
			nav: true,
			dots: false,
			URLhashListener: false,
			autoplayHoverPause: false,
			startPosition: 'URLHash',
			responsive: {
				0: {
					items: 1,
				},
				1200: {
					center: true,
					margin: 10,
					items: 3,
				}
			}
		});
	});

	$('.lp-featured-block__close').on('click', function() {
		$(this).closest('.lp-featured-block').removeClass('slider--active');
		$(this).closest('.lp-featured-block').find('.lp-featured-block__slider').owlCarousel('destroy');
	});

	$(document).on("keyup", function(e) {
		if (e.key === "Escape") {
			$(".lp-featured-block__close").trigger("click");
		}
	});
});
</script>